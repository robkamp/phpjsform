    static function postSlug($str) 
    { 
      return strtolower(preg_replace(array('/[^a-zA-Z0-9 -_]/', '/[ -]+/', '/^-|-$/'), 
      array('', '-', ''), utils::removeAccent($str))); 
    }     
    
    
    static function removeWhitespace($str) {

      $search = array('/ ( )*/'); // Seacrh for all eccessive whitespace
      $replace = array(' '); // Replace with one space
      $str = preg_replace($search,$replace,$str); // Execute

      $search = array('/ *</','/> */'); // Search for alle elemets preceded and trailed by one or more spaces 
      $replace = array('<','>'); // Replace with nothing
      $str = preg_replace($search,$replace,$str); // Execute 

      $search = array('/<tr>\s+<\/tr>/','/<tr><td>\s+<\/td><\/tr>/'); // Search for empty rows
      $replace = array('',''); // Replace with nothing
      $str = preg_replace($search,$replace,$str); // Execute 
      
      $search = array('/<table>\s+<\/table>/'); // Search for empty tables 
      $replace = array(''); // Replace with none
      $str = preg_replace($search,$replace,$str); // Execute 
      return $str;
